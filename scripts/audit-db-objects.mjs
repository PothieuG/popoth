#!/usr/bin/env node
// Sprint Cleanup-Legacy / C2.
// Generic audit covering 5 categories of public.* DB objects:
//   1. functions (pg_proc.prokind IN ('f','a','p')) — incl. plain, aggregate, procedure
//   2. composite types (pg_type.typtype='c', excluding implicit row-types of tables)
//   3. enums (pg_type.typtype='e')
//   4. domains (pg_type.typtype='d')
//   5. operators (pg_operator)
//
// For each category: enumerate prod via pg_catalog and regrep supabase/migrations/
// for the matching CREATE statement. Anything in prod with no CREATE in
// migrations is flagged as "MISSING_FROM_MIGRATIONS" — same pattern A2 / B1.
//
// Why this exists vs db:audit-functions:
//   - db:audit-functions is the same idea but only for pg_proc functions.
//   - Custom types, enums, domains, operators can land in prod via a hand-crafted
//     CREATE TYPE/CREATE DOMAIN/CREATE OPERATOR run outside of supabase/migrations,
//     escaping the function-only audit. This script closes that gap.
//
// Not added to the weekly cron nor PR-time gate — heavier than check-* detectors,
// and the categories beyond functions are typically empty on most schemas. Run
// after any migration introducing a CREATE TYPE / CREATE DOMAIN / CREATE OPERATOR.
//
// Usage:
//   $env:SUPABASE_ACCESS_TOKEN = "sbp_..."
//   pnpm db:audit-objects
//
// Exit 0 -> every public.* object in scanned categories is referenced by at
//           least one migration file.
// Exit 1 -> at least one prod object has no CREATE statement in
//           supabase/migrations/.
// Exit 2 -> fatal (network, auth, etc.).

import { readdirSync, readFileSync } from 'node:fs'
import { join } from 'node:path'

const PROJECT_REF = process.env.SUPABASE_PROJECT_REF ?? 'jzmppreybwabaeycvasz'
const TOKEN = process.env.SUPABASE_ACCESS_TOKEN

if (!TOKEN) {
  console.error('ERROR: set $env:SUPABASE_ACCESS_TOKEN before running.')
  process.exit(2)
}

const URL_API = `https://api.supabase.com/v1/projects/${PROJECT_REF}/database/query`
const MIGRATIONS_DIR = 'supabase/migrations'

async function query(sql) {
  const res = await fetch(URL_API, {
    method: 'POST',
    headers: {
      Authorization: `Bearer ${TOKEN}`,
      'Content-Type': 'application/json',
    },
    body: JSON.stringify({ query: sql }),
  })
  if (!res.ok) {
    const body = await res.text()
    throw new Error(`Management API ${res.status}: ${body}`)
  }
  return res.json()
}

function escapeRegex(s) {
  return s.replace(/[.*+?^${}()|[\]\\]/g, '\\$&')
}

function loadMigrationCorpus() {
  const files = readdirSync(MIGRATIONS_DIR).filter((f) => f.endsWith('.sql'))
  const parts = files.map((f) => readFileSync(join(MIGRATIONS_DIR, f), 'utf8'))
  return { files, corpus: parts.join('\n') }
}

// Exclude implicit row-types of tables/views/matviews/foreign tables/partitioned
// tables — those are auto-generated by CREATE TABLE etc., not user types.
const COMPOSITE_TYPE_FILTER = `NOT EXISTS (
  SELECT 1 FROM pg_class c
  WHERE c.oid = t.typrelid
    AND c.relkind IN ('r','v','m','f','p')
)`

const CATEGORIES = [
  {
    label: 'function',
    sql: `
      SELECT p.proname AS objname
      FROM pg_proc p
      JOIN pg_namespace n ON n.oid = p.pronamespace
      WHERE n.nspname = 'public'
        AND p.prokind IN ('f','a','p')
      ORDER BY p.proname
    `,
    regex: (name) =>
      new RegExp(
        `CREATE\\s+(?:OR\\s+REPLACE\\s+)?FUNCTION\\s+(?:public\\.)?${escapeRegex(name)}\\b`,
        'i'
      ),
  },
  {
    label: 'composite_type',
    sql: `
      SELECT t.typname AS objname
      FROM pg_type t
      JOIN pg_namespace n ON n.oid = t.typnamespace
      WHERE n.nspname = 'public'
        AND t.typtype = 'c'
        AND ${COMPOSITE_TYPE_FILTER}
      ORDER BY t.typname
    `,
    regex: (name) =>
      new RegExp(`CREATE\\s+TYPE\\s+(?:public\\.)?${escapeRegex(name)}\\b`, 'i'),
  },
  {
    label: 'enum',
    sql: `
      SELECT t.typname AS objname
      FROM pg_type t
      JOIN pg_namespace n ON n.oid = t.typnamespace
      WHERE n.nspname = 'public'
        AND t.typtype = 'e'
      ORDER BY t.typname
    `,
    regex: (name) =>
      new RegExp(
        `CREATE\\s+TYPE\\s+(?:public\\.)?${escapeRegex(name)}\\s+AS\\s+ENUM`,
        'i'
      ),
  },
  {
    label: 'domain',
    sql: `
      SELECT t.typname AS objname
      FROM pg_type t
      JOIN pg_namespace n ON n.oid = t.typnamespace
      WHERE n.nspname = 'public'
        AND t.typtype = 'd'
      ORDER BY t.typname
    `,
    regex: (name) =>
      new RegExp(`CREATE\\s+DOMAIN\\s+(?:public\\.)?${escapeRegex(name)}\\b`, 'i'),
  },
  {
    label: 'operator',
    sql: `
      SELECT o.oprname AS objname
      FROM pg_operator o
      JOIN pg_namespace n ON n.oid = o.oprnamespace
      WHERE n.nspname = 'public'
      ORDER BY o.oprname
    `,
    regex: (name) =>
      new RegExp(`CREATE\\s+OPERATOR\\s+(?:public\\.)?${escapeRegex(name)}\\b`, 'i'),
  },
]

async function main() {
  const { files, corpus } = loadMigrationCorpus()

  const results = []
  for (const cat of CATEGORIES) {
    const rows = await query(cat.sql)
    const names = rows.map((r) => r.objname)
    const versioned = names.filter((n) => cat.regex(n).test(corpus))
    const missing = names.filter((n) => !cat.regex(n).test(corpus))
    results.push({ label: cat.label, names, versioned, missing })
  }

  console.error(`Scanned ${files.length} migration files for 5 public.* object categories.\n`)

  // Summary table
  const labelWidth = Math.max(8, ...results.map((r) => r.label.length))
  const headerCols = `${'category'.padEnd(labelWidth)}  in_pg_catalog  in_migrations  missing`
  process.stdout.write(headerCols + '\n')
  process.stdout.write('-'.repeat(headerCols.length) + '\n')
  for (const r of results) {
    process.stdout.write(
      `${r.label.padEnd(labelWidth)}  ${String(r.names.length).padEnd(13)}  ${String(r.versioned.length).padEnd(13)}  ${r.missing.length}\n`
    )
  }
  process.stdout.write('\n')

  // Per-category breakdown (only when there are entries)
  for (const r of results) {
    if (r.names.length === 0) continue
    process.stdout.write(`[${r.label}] ${r.names.length} object(s):\n`)
    const nameWidth = Math.max(4, ...r.names.map((n) => n.length))
    for (const name of r.names) {
      const flag = r.missing.includes(name) ? 'MISSING' : 'ok'
      process.stdout.write(`  ${name.padEnd(nameWidth)}  ${flag}\n`)
    }
    process.stdout.write('\n')
  }

  const allMissing = results.flatMap((r) => r.missing.map((n) => `${r.label}: ${n}`))

  if (allMissing.length === 0) {
    console.error('OK: every public.* object across 5 categories is versioned in supabase/migrations/.')
    process.exitCode = 0
    return
  }

  console.error(
    `MISSING_FROM_MIGRATIONS: ${allMissing.length} object(s) exist in prod with no CREATE statement in supabase/migrations/:`
  )
  for (const entry of allMissing) console.error(`  - ${entry}`)
  console.error('')
  console.error('To capture retroactively (Sprint Audit-Triggers / A2 pattern, see CLAUDE.md §8):')
  console.error('  1. Dump the object definition (pg_get_functiondef / pg_dump --schema-only / etc.)')
  console.error('  2. Paste verbatim into a new <TS>_capture_<name>.sql migration.')
  console.error('  3. node scripts/apply-sql.mjs <migration> (idempotent if possible).')
  console.error('  4. pnpm supabase migration repair --status applied <TS>')
  console.error('  5. Re-export baseline + re-run pnpm db:audit-objects to confirm exit 0.')
  process.exitCode = 1
}

// Use process.exitCode (not process.exit) so Node drains the undici keep-alive
// sockets cleanly. process.exit() while sockets are closing triggers a libuv
// assertion on Windows.
main().catch((err) => {
  console.error('FATAL:', err.message)
  process.exitCode = 2
})
